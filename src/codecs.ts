interface CodecTable {
  decode: readonly number[];
  encode: Map<number, number>;
}

function buildEncodeMap(decodeTable: readonly number[]): Map<number, number> {
  const map = new Map<number, number>();
  for (let i = 0; i < 0x80; i++) {
    map.set(i, i);
  }
  for (let i = 0; i < decodeTable.length; i++) {
    const cp = decodeTable[i]!;
    const byte = i + 0x80;
    // first mapping wins -- real charset entry takes priority over sloppy fallback
    if (!map.has(cp)) {
      map.set(cp, byte);
    }
  }
  return map;
}

function buildSloppyTable(table: readonly number[]): readonly number[] {
  const result = [...table];
  for (let i = 0; i < result.length; i++) {
    if (result[i] === 0xfffd) {
      result[i] = i + 0x80;
    }
  }
  return result;
}

